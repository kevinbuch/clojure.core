package clojure.lang.platform;

public class NumberOps {

  public static int shortAdd(short x, short y) {
    return x + y;
  }

  public static int intAdd(int x, int y) {
    return x + y;
  }

  public static long longAdd(long x, long y) {
    return x + y;
  }

  public static double doubleAdd(double x, double y) {
    return x + y;
  }

  public static long longBitXor(long x, long y) {
    return x ^ y;
  }

  public static long longBitUnsignedShiftRight(long x, long y) {
    return x >>> y;
  }

  public static int primativeInt(Number x) {
    return x.intValue();
  }
}
